import java.util.*;
import java.io.*;
class Solution{
    public static void main(String[ ] args){
          Scanner sc = new Scanner(System.in);
          float p = sc.nextFloat();
          float r = sc.nextFloat();
          int t = sc.nextInt();
          float s = (p*r*t)/100;
          System.out.println(s+" ");
    }
}
